package com.parcialGDB.parcialGDB.model;

public class Cliente {
    private String clie_nombre;
    private String clie_telefono;
    private String clie_direccion;
    private int clie_edad;
    private String clie_sexo;
    private String clie_documento;

    public String getClie_nombre() {
        return clie_nombre;
    }

    public void setClie_nombre(String clie_nombre) {
        this.clie_nombre = clie_nombre;
    }

    public String getClie_telefono() {
        return clie_telefono;
    }

    public void setClie_telefono(String clie_telefono) {
        this.clie_telefono = clie_telefono;
    }

    public String getClie_direccion() {
        return clie_direccion;
    }

    public void setClie_direccion(String clie_direccion) {
        this.clie_direccion = clie_direccion;
    }

    public int getClie_edad() {
        return clie_edad;
    }

    public void setClie_edad(int clie_edad) {
        this.clie_edad = clie_edad;
    }

    public String getClie_sexo() {
        return clie_sexo;
    }

    public void setClie_sexo(String clie_sexo) {
        this.clie_sexo = clie_sexo;
    }

    public String getClie_documento() {
        return clie_documento;
    }

    public void setClie_documento(String clie_documento) {
        this.clie_documento = clie_documento;
    }
}
